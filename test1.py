import re

def dms2dec(dms):
      match = re.search(r'([0-9]+)([0-9]{2})([0-9]{2}\.[0-9]+)$', dms)
      dms_d = float(match.group(1))
      dms_m = float(match.group(2))
      dms_s = float(match.group(3))
      return dms_d + dms_m/60 + dms_s/3600

